function yourName() {
    let yourName = prompt('Как тебя зовут?');
    alert(`Привет, ${yourName}!`);
}

function plus() {
    let a = prompt('Введите первое значение');
    let b = prompt('Введите второе значение');

    alert(Number(a) + Number(b));
}

function minus() {
    let a = prompt('Введите первое значение');
    let b = prompt('Введите второе значение');

    alert(Number(a) - Number(b));
}

function times() {
    let a = prompt('Введите первое значение');
    let b = prompt('Введите второе значение');

    alert(Number(a) * Number(b));
}

function divide() {
    let a = prompt('Введите первое значение');
    let b = prompt('Введите второе значение');

    alert(Number(a) / Number(b));
}